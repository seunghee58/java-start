package variable.ex;

public class VarEx3 {

    public static void main(String[] args) {
        /*
        long 타입의 변수를 선언하고, 그 변수를 10000000000(백억)으로 초기화한 후 출력하는 프로그램을 작성하세요.
        boolean 타입의 변수를 선언하고, 그 변수를 true 로 초기화한 후 출력하는 프로그램을 작성하세요.
        */
        long l = 100000000L;
        System.out.println(l);

        boolean b = true;
        System.out.println(b);
    }
}
